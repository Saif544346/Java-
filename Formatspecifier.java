
package com.mycompany.ooplab;

public class Formatspecifier {
    public static void main(String[] args) {
         int a=10;
        float b=10.4f;
        double c=11.4;
        char d='s';
        short e=22100;
        boolean f=true;
        System.out.printf("int %d\n",a);
        System.out.printf("float %f\n",b);
        System.out.printf("double %.2f\n",c);
        System.out.printf("cahr %c\n",d);
        System.out.printf("short %d\n",e);
        System.out.printf("boolean %b\n",f);
    }
    
}
